void sparki_my_setpixel(uint8_t x, uint8_t y, uint8_t color) { // "private"
    sparki_ensure_lcd_init();
    if ((x >= LCDWIDTH) || (y >= LCDHEIGHT))
        return;

    // x is which column
    if (color)
        st7565_buffer[x+ (y/8)*128] |= _BV(7-(y%8));
    else
        st7565_buffer[x+ (y/8)*128] &= ~_BV(7-(y%8));
}

